export const query = graphql`
  query {
    allMarkdownRemark(filter: {fileAbsolutePath: {regex: "/begin-journey/"}}) {
      nodes {
        fields {
          slug
        }
        excerpt(truncate: true, pruneLength: 100)
      }
    }
  }
`;
